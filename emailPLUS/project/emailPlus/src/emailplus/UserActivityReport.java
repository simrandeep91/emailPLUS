    private void btnListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListActionPerformed
        // TODO add your handling code here:
        String str=ddlUsername.getSelectedItem().toString();
        if(str.equals("Select Username"))
        {
            JOptionPane.showMessageDialog(this, "select a username", "Error", 2);
        }
        else
        {
            String header[]={"User ID","Username","Login Time","Logout Time"};
            Object data[][]=new Object[al.size()][4];
            int j=0;
            for(int i=0;i<al.size();i++)
            {
                UserActivityBean objBean=(UserActivityBean)al.get(i);
                if(str.equals(objBean.getUsername()))
                {
                    data[j][0]=objBean.getUserId();
                    data[j][1]=objBean.getUsername();
                    data[j][2]=objBean.getLoginTime();
                    data[j][3]=objBean.getLogoutTime();
                    j++;
                }
            }
            UserActivityTable=new JTable(data,header);
            jScrollPane.setViewportView(UserActivityTable);
            if(j==0)
            {
                JOptionPane.showMessageDialog(this, "empty record", "Error", 2);
            }
        }
    }//GEN-LAST:event_btnListActionPerformed

