     public void generateTable()
    {
        String header[]={"User ID","Username","Login Time","Logout Time"};
        Object data[][]=new Object[al.size()][4];
        for(int i=0;i<al.size();i++)
        {
            UserActivityBean objBean=(UserActivityBean)al.get(i);
            data[i][0]=objBean.getUserId();
            data[i][1]=objBean.getUsername();
            data[i][2]=objBean.getLoginTime();
            data[i][3]=objBean.getLogoutTime();
        }
        UserActivityTable=new JTable(data,header);
        jScrollPane.setViewportView(UserActivityTable);
    }
     public void addUsername()
     {
         ArrayList userlist=obj.getAllUsername();
         for(int i=0;i<userlist.size();i++)
         {
             String str=(String)userlist.get(i);
             ddlUsername.addItem(str);
         }
     }

